            FileLogger.WriteLn($"postDeserializationMethodName = '{postDeserializationMethodName}'");
#endif

            sourceCodeBuilder.AppendLine();
            sourceCodeBuilder.AppendLine($"namespace {targetClassItem.Namespace}");
